#include <bits/stdc++.h>
using namespace std;
int main()
{
    char a = 'a';
    char c = 'c';
    char d = 'd';
    char z = 'z';
    cout << (int)a << " : " << (int)c << " : "
         << (int)d << " : " << (int)z << '\n';
    cout << (int)a - 97 << '\n';
    cout << (int)a - 'a' << '\n';
    cout << (int)c - 99 << '\n';
    cout << (int)c - 'c' << '\n';
    cout << (int)d - 100 << '\n';
    cout << (int)d - 'd' << '\n';
    cout << (int)z - 122 << '\n';
    cout << (int)z - 'z' << '\n';
    return 0;
}